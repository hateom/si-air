//////////////////////////////////////////////////////////////////////////

void OptionsBox::remove_last_cfg_form()
{
	if( last != NULL )
	{
		delete last;
		last = NULL;
	}
}

//////////////////////////////////////////////////////////////////////////

void OptionsBox::set_current_cfg_form( OptForm * of )
{
	last = of;
}

