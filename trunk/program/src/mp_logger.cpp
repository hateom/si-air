#include "mp_logger.h"
#include <cstdarg>

//////////////////////////////////////////////////////////////////////////

int mpLogger::output_type = OT_NONE;

mpLogger::mpLogger()
{
}

//////////////////////////////////////////////////////////////////////////

mpLogger::~mpLogger()
{
}

//////////////////////////////////////////////////////////////////////////

void mpLogger::set_output( int ot )
{
	output_type = ot;
}

//////////////////////////////////////////////////////////////////////////

void mpLogger::log( std::string text )
{
	log( text.c_str() );
}

//////////////////////////////////////////////////////////////////////////

void mpLogger::log( const char * text, ... )
{
	va_list al;
	static char buffer[256] = "";

	va_start( al, text );
	vsprintf( buffer, text, al );
	va_end( al );

	switch( output_type )
	{
	case OT_CONSOLE:
		printf( buffer );
		break;
	case OT_WINDOW:
		/// under construction
		break;
	default:
		/// none
		break;
	}
}

